<script>
export default {
  data() {
    return {
      orders: [], // 订单列表
      total: [],// 每个订单的商品数量及总价列表
      status: [] //每个订单的支付状态
    };
  },
  activated() {
    // 获取订单数据
    this.$axios
      .post("http://47.100.75.161:8081/order/getOrder", {
        userId: this.$store.getters.getUser.userId
      })
      .then(res => {
        if (res.data.code === "001") {
          this.orders = res.data.result;
          this.orders.reverse();
        }
        // else {
        //   this.notifyError(res.data.msg);
        // }
      })
      .catch(err => {
        return Promise.reject(err);
      });
  },
  watch: {
    // 通过订单信息，计算出每个订单的商品数量及总价
    orders: function(val) {
      let total = [];
      for (let i = 0; i < val.length; i++) {
        const element = val[i];

        let totalNum = 0;
        let totalPrice = 0;
        for (let j = 0; j < element.length; j++) {
          const temp = element[j];
          totalNum += temp.num;
          totalPrice += temp.price * temp.num;
        }
        total.push({ totalNum, totalPrice });
      }
      this.total = total;
    }
  },
  methods:{
    getStatus(status){
    if(status==0){
      return "去付款"
    }
    if(status==1){
        return "已支付"
    }
    if(status==3){
        return "已取消"
    }
},
    changeStyle(status){
      if(status==0){
        return true;
      }else{
        return false;
      }
    },
    pay(uuid,orderName,total,status) {
      if(status == 0){
        this.$axios
            .get("http://47.100.75.161:8081/pay/alipay", {
              params: {
                orderId: uuid,
                total: total,
                orderName: orderName,
              }
            }).then(successResponse => {
          document.querySelector('body').innerHTML = successResponse.data;//查找到当前页面的body，将后台返回的form替换掉他的内容
          document.forms[0].submit();
        }).catch();
      }
    }

  }
};
</script>
